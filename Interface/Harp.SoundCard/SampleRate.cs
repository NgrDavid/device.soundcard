        /// Specifies a sampling rate of 96 kHz.
        /// </summary>
        SampleRate96000Hz = 96000,

        /// <summary>
        /// Specifies a sampling rate of 192 kHz.
        /// </summary>
        SampleRate192000Hz = 192000
    }
}
